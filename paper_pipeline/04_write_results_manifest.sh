#!/usr/bin/env bash
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$REPO_ROOT"

MAIN_RUN="${1:-$(cat outputs/paper_runs/latest_main_run.txt)}"
ABLATION_RUN="${2:-$(cat outputs/paper_runs/latest_ablation_run.txt)}"
PAPER_RUN_DIR="${3:-outputs/paper_runs/manual_$(date -u +%Y%m%d_%H%M%S)}"
AGENTIC_RUN="${4:-}"
AGENTIC_ABLATION_RUN="${5:-}"
MANIFEST="$PAPER_RUN_DIR/PAPER_RESULTS.md"

mkdir -p "$PAPER_RUN_DIR"

cat > "$MANIFEST" <<EOF
# SolarChain-Eval Paper Results

Generated at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

## Paper Pipeline Version

This manifest is for the KDD Workshop on Evaluation and Trustworthiness of Agentic AI submission pipeline.

Primary benchmark configuration:

\`\`\`text
configs/month_2026_04.yaml
\`\`\`

Primary dataset:

\`\`\`text
data/datasets_2026_04_month
\`\`\`

The dataset covers five cities (Beijing, Shanghai, Chengdu, Shenzhen, Hangzhou) over \`[2026-04-01, 2026-05-01)\`, with 720 hourly timestamps. Training keeps \`episode_steps=24\`; each environment reset samples one complete day from the month.

Dataset metadata, when generated by \`02_run_paper_experiments.sh\`:

- \`$PAPER_RUN_DIR/dataset_summary.json\`

## Main Experiment

Run directory:

\`\`\`text
$MAIN_RUN
\`\`\`

Primary data files:

- \`$MAIN_RUN/summary.json\`
- \`$MAIN_RUN/metrics.csv\`
- \`$MAIN_RUN/actions.csv\`
- \`$MAIN_RUN/city_hour_policy.csv\`
- \`$MAIN_RUN/config_snapshot.json\`
- \`$MAIN_RUN/run_metadata.json\`

Trained models:

- \`$MAIN_RUN/models/ppo/ppo_model.zip\`
- \`$MAIN_RUN/models/sac/sac_model.zip\`
- \`$MAIN_RUN/models/dqn/dqn_model.zip\`

Main figures:

- \`$PAPER_RUN_DIR/figures/main/learning_curves.png\`
- \`$PAPER_RUN_DIR/figures/main/safety_utility_frontier.png\`
- \`$PAPER_RUN_DIR/figures/main/city_hour_liquidity_heatmap.png\`

## No-Physics-Penalty Ablation

Run directory:

\`\`\`text
$ABLATION_RUN
\`\`\`

Primary data files:

- \`$ABLATION_RUN/summary.json\`
- \`$ABLATION_RUN/metrics.csv\`
- \`$ABLATION_RUN/actions.csv\`
- \`$ABLATION_RUN/city_hour_policy.csv\`
- \`$ABLATION_RUN/config_snapshot.json\`
- \`$ABLATION_RUN/run_metadata.json\`

Ablation figures:

- \`$PAPER_RUN_DIR/figures/ablation_no_physics_penalty/learning_curves.png\`
- \`$PAPER_RUN_DIR/figures/ablation_no_physics_penalty/safety_utility_frontier.png\`
- \`$PAPER_RUN_DIR/figures/ablation_no_physics_penalty/city_hour_liquidity_heatmap.png\`

## Eval-Only Agentic Layer

The agentic comparison evaluates trained RL policies with an evaluation-only LLM Planner + LLM Auditor between policy prediction and \`env.step()\`. Training remains unchanged.

Agentic main run directory:

\`\`\`text
${AGENTIC_RUN:-not generated}
\`\`\`

Agentic no-physics run directory:

\`\`\`text
${AGENTIC_ABLATION_RUN:-not generated}
\`\`\`

Agentic files, when generated:

- \`${AGENTIC_RUN:-<agentic_run>}/summary.json\`
- \`${AGENTIC_RUN:-<agentic_run>}/metrics.csv\`
- \`${AGENTIC_RUN:-<agentic_run>}/actions.csv\`
- \`${AGENTIC_RUN:-<agentic_run>}/agentic_logs.jsonl\`
- \`${AGENTIC_ABLATION_RUN:-<agentic_ablation_run>}/summary.json\`
- \`${AGENTIC_ABLATION_RUN:-<agentic_ablation_run>}/agentic_logs.jsonl\`
- \`$PAPER_RUN_DIR/figures/agentic/*.png\`
- \`$PAPER_RUN_DIR/figures/agentic_no_physics_penalty/*.png\`

## Metrics To Report

Batch-level metadata:

- \`$PAPER_RUN_DIR/paper_run_metadata.json\`
- \`$PAPER_RUN_DIR/dataset_summary.json\`
- \`$PAPER_RUN_DIR/PAPER_RESULTS.md\`

Use \`summary.json\` for the main comparison table:

- cumulative_reward
- physics_violation_rate
- max_drawdown
- action_jitter
- slippage_reduction_vs_static
- spatial_fairness_index
- artificial_liquidity_MWh

Use the ablation \`summary.json\` to compare whether removing the \`P_max\` penalty increases \`physics_violation_rate\` and \`artificial_liquidity_MWh\`.

Use agentic \`summary.json\` files for the KDD Workshop agentic-AI evaluation claims:

- plan_validity_rate
- audit_call_rate
- revision_rate
- action_modification_rate
- avg_action_delta_from_auditor
- llm_failure_count
- mock_llm_used

If \`mock_llm_used=true\`, treat the run as a software smoke check rather than a final LLM result.

## Figure Mapping

- Learning curves: reward vs. episode for PPO, SAC, DQN, Static 1:3, Random, Myopic Greedy.
- Safety-utility frontier: physics violation rate vs. cumulative reward.
- Spatio-temporal heatmap: RL liquidity split by city/hour compared to Static 1:3.
- Agentic figures: same plot types for RL policies evaluated with the planner/auditor layer.

EOF

echo "Wrote $MANIFEST"

